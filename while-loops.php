<?php
  // =============
  // while loops
  // =============
  // *** Same usage as JavaScript ***
  
  // While loops run some code infinitely while some condition remains true.

  // To stop the code in a while loop from running, you need to write some code to make
  // the initial condition false.
  
  $counter = 0;

  while ($counter < 10) {
    $counter++;
    echo $counter . "<br>";
  }
?>